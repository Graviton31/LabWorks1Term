# Задание 11: Дан массив, поменять знак у элементов, значения которых между 4 и 7
print("Задание 11:")

arr11 = np.array([1, 5, 6, 8, 4, 3, 7, 2, 4.5, 7.5])
print("Исходный массив:", arr11)
# Меняем знак у элементов, которые находятся в интервале (4, 7) - исключая границы? Строго "между" - обычно не включая границы.
# Но в задании сказано "между 4 и 7" - можно трактовать как 4 < x < 7.
mask = (arr11 > 4) & (arr11 < 7)
arr11[mask] = -arr11[mask]
print("Массив после смены знака у элементов между 4 и 7:", arr11)
print()

